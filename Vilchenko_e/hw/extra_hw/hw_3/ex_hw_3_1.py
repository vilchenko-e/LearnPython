a = [1, 5, 3]
b = [1, 5, 1, 5]
c = [1, 3, 1, 5, 3, 3]

# добавляем первый побочный и считаем пятёрки
a.extend(b)
count_5 = a.count(5)
print(f"Кол-во цифр 5 при первом объединении: {count_5}")

# удаляем все пятёрки
while 5 in a:
    a.remove(5)

# добавляем второй побочный и считаем тройки
a.extend(c)
count_3 = a.count(3)
print(f"Кол-во цифр 3 при втором объединении: {count_3}")

print(f"Итоговый список: {a}")
