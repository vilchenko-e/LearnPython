def smallest_divisor(n: int) -> int:
    """наименьший делитель числа n, отличный от 1"""
    # если число чётное, сразу возвращаем 2
    if n % 2 == 0:
        return 2
    # проверяем нечётные делители до корня из n
    i = 3
    while i * i <= n:
        if n % i == 0:
            return i
        i += 2
    # если делитель не найден, число простое
    return n


num = int(input("Введите число: "))
print(f"Наименьший делитель, отличный от единицы: {smallest_divisor(num)}")
